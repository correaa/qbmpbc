function symbol = slookup(anum)
%
% usage: symbol = slookup(anum)
% returns the atomic symbol associated with the input atomic number.
%
if (isnumeric(anum))
  switch anum
     case 1
       symbol = 'H';
     case 2
       symbol = 'He';
     case 3
       symbol = 'Li';
     case 4
       symbol = 'Be';
     case 5
       symbol = 'B';
     case 6
       symbol = 'C';
     case 7
       symbol = 'N';
     case 8
       symbol = 'O';
     case 9
       symbol = 'F';
     case 10
       symbol = 'Ne';
     case 11
       symbol = 'Na';
     case 12
       symbol = 'Mg';
     case 13
       symbol = 'Al';
     case 14
       symbol = 'Si';
     case 15
       symbol = 'P';
     case 16
       symbol = 'S';
     case 17
       symbol = 'Cl';
     case 18
       symbol = 'Ar';
     case 19
       symbol = 'K';
     case 20 
       symbol = 'Ca';
     case 21 
       symbol = 'Sc';
     case 22 
       symbol = 'Ti';
     case 23 
       symbol = 'V';
     case 24 
       symbol = 'Cr';
     case 25 
       symbol = 'Mn';
     case 26 
       symbol = 'Fe';
     case 27 
       symbol = 'Co';
     case 28 
       symbol = 'Ni';
     case 29 
       symbol = 'Cu';
     case 30 
       symbol = 'Zn';
     case 31 
       symbol = 'Ga';
     case 32 
       symbol = 'Ge';
     case 33 
       symbol = 'As';
     case 34 
       symbol = 'Se';
     case 35 
       symbol = 'Br';
     case 36
       symbol = 'Kr';
     case 78 
       symbol = 'Pt';
  end
else
   error('the input must be an atomic symbol');
end;
