#ifndef BOOST_STRING_CAST_
#define BOOST_STRING_CAST_
 
#include<boost/lexical_cast.hpp>
namespace boost{
	template<typename T>
	std::string string_cast(T const& t){
		return boost::lexical_cast<std::string>(t);
	}
	template<typename T>
	std::string to_string(T const& t){
		return boost::lexical_cast<std::string>(t);
	}
}
#endif
